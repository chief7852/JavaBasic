package e_oop;

public class Calculator {
	//두개의 파라미터를 받아서 연산자로 계산하는 식
	//연산자 사용 '+', '-', '*', '/', '%'

	//메서드 :
	
	double plus(double x,double y){
		return x+y;
	}
	double minus(double x,double y){
		return x-y;
	}
	double multi(double x,double y){
		return x*y;
	}
	double divi(double x,double y){
		return x/y;
	}
	double remain(double x,double y){
		return x%y;
	}
}
